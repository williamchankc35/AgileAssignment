    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        //AD          
        AffID = Aff.getAffID();       
        AffPass = Aff.getAffPass();
        AffName = Aff.getAffName();
        AffAdd = Aff.getAffAdd();
        OwnerName = Aff.getOwnerName();
        
        //MD
        FoodID = Menu.getFoodID();
        FoodName = Menu.getFoodName();
        Price = Menu.getPrice();
        Description =Menu.getDescription();
        Priority = Menu.getPriority();
        ID = Menu.getID();
        //other 
        int tmp =0;
        String tmpAcc="";
        String status ="";
        
        for(int i =0 ; i< AffID.length ; i ++ ){
            if(JtfID.getText().equals(AffID[i]))
                for(int j =0 ; j< AffPass.length ; j ++ ){
                     if(JtfPassword.getText().equals(AffPass[j]))                    
                     status ="Succesful" ;   
                     tmpAcc = AffName[i];
                     tmp = i;
        }
         
        }
       
        if(!"Succesful".equals(status)){
        JOptionPane.showMessageDialog(Login,"Wrong ID and Password");
        }
        else{
        JOptionPane.showMessageDialog(Login," Welcome ! " + tmpAcc);
              
              jtfResID.setEditable(false);
              jtfResID.setText(AffID[tmp]);
              jtfResName.setEditable(false);
              jtfResName.setText(AffName[tmp]);
              jtfResLoc.setEditable(false);
              jtfResLoc.setText(AffAdd[tmp]);
              jtfResOwner.setEditable(false);
              jtfResOwner.setText(OwnerName[tmp]);
              jPanel2.setVisible(true);
              jPanel4.setVisible(true);
              jPanel1.setVisible(false);
         
             for(int k=0; k<ID.length;k++){
             if(AffID[tmp].equals(ID[k])){
              jComboBox1.addItem(FoodID[k]);       
             }           
             }
        }
    }//GEN-LAST:event_LoginActionPerformed

